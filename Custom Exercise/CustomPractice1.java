public class CustomPractice1 {
    public static void main(String[] args){
        int var1 = 20;
        int var2 = 10;
    
        //ref
        System.out.println("First Variable value is "+ var1);
        System.out.println("Second Variable value is "+ var2);

        //Adding
        System.out.println("Adding...");
        System.out.println("The answer is "+ (var1 + var2));

        //subtact
        System.out.println("Subtracting...");
        System.out.println("The answer is "+ (var1 - var2));

        //Multiplication
        System.out.println("Multiplying...");
        System.out.println("The answer is "+ (var1 * var2));

        //Division
        System.out.println("Dividing...");
        System.out.println("The answer is "+ (var1 / var2));
    }
}
